"""Prompt registry — the library of prompts, each addressed by id (story afe6).

The reviewer-centric model has been collapsed into a UNIFIED prompt model: every
prompt is a front-matter'd ``*.md`` file, and ``category: review`` is the EXPLICIT
flag that marks a prompt as a reviewer. :func:`get_prompt` is the single resolver
returning a :class:`Prompt`; the derived :data:`reviewers/index.json` (generated by
:func:`regenerate_prompt_index`) is the offline-testable selection catalog.

Two concerns are kept deliberately separate (the converging OSS pattern):

  * **Reviewer identity + selection rules** (id, dimension, file-glob ``applies_to``,
    whether it's a default) live in a DERIVED, versioned, testable local index
    (``reviewers/index.json``, scanned from the prompt front-matter) — so selection
    is code-reviewed and offline-testable.
  * **Prompt text is GIT-CANONICAL** (epic a88f / WS-F1): the committed prompt file
    is the single source of truth — a packaged ``reviewers/*.md`` (or a user override
    at ``.rebar/prompts/<id>.md``). **Langfuse is NEVER consulted for prompt text**
    (read-replica only); the resolved text's content hash (sha256) is returned so it
    can be embedded in traces and any divergence from a Langfuse copy is detectable.
    Rendering is **strict** — an unsupplied ``{{var}}`` raises, never a silent empty.

Stdlib-only.
"""

